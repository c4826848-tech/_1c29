function shuffle(arr){
  for(let i = arr.length - 1; i > 0; i--){
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function shuffleAnswers(q){
  const opts = q.a.map((text, i) => ({ text, i }));
  shuffle(opts);
  const newCorrect = opts.findIndex(x => x.i === q.c);

  return {
    question: q.q,
    answers: opts.map(x => x.text),
    correct: newCorrect
  };
}

function takeRandom(pool, count){
  const copy = [...pool];
  shuffle(copy);
  return copy.slice(0, count);
}

// نحتاج 15 مباراة × (2 سهلة + 4 متوسطة + 4 صعبة) = 150 سؤال
function buildTournamentQuestions(matchCount = 15){
  const needEasy = matchCount * 2;   // 30
  const needMed  = matchCount * 4;   // 60
  const needHard = matchCount * 4;   // 60

  if(window.Q_EASY.length < needEasy){
    throw new Error("عدد الأسئلة السهلة غير كافٍ.");
  }
  if(window.Q_MED.length < needMed){
    throw new Error("عدد الأسئلة المتوسطة غير كافٍ.");
  }
  if(window.Q_HARD.length < needHard){
    throw new Error("عدد الأسئلة الصعبة غير كافٍ.");
  }

  const easy = takeRandom(window.Q_EASY, needEasy);
  const med  = takeRandom(window.Q_MED,  needMed);
  const hard = takeRandom(window.Q_HARD, needHard);

  const out = [];

  for(let m = 0; m < matchCount; m++){
    const batch = [
      easy[m*2 + 0],
      easy[m*2 + 1],

      med[m*4 + 0],
      med[m*4 + 1],
      med[m*4 + 2],
      med[m*4 + 3],

      hard[m*4 + 0],
      hard[m*4 + 1],
      hard[m*4 + 2],
      hard[m*4 + 3]
    ];

    shuffle(batch);
    batch.forEach(q => out.push(shuffleAnswers(q)));
  }

  return out;
}

